CREATE TABLE CALENDAR(
CALENDAR_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
WORKING_DATE DATE,
DAY VARCHAR(50),
WEEK INTEGER,
DESCRIPTION VARCHAR(100),
USER_ID INTEGER);

CREATE TABLE HOTEL(
HOTEL_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
HOTEL_NAME VARCHAR(128),
DESCRIPTION VARCHAR(128)
);

CREATE TABLE PLAN(
PLAN_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
USER_ID INTEGER,
HOTEL_ID INTEGER
);

CREATE TABLE POSITIONS(
POSITION_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
DESCRIPTION VARCHAR(200)
);

CREATE TABLE USER(
USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
FIRSTNAME VARCHAR(100),
LASTNAME VARCHAR(100),
AGE INTEGER,
SEX VARCHAR(20),
USERNAME VARCHAR(100),
PASSWORD VARCHAR(200),
SALT varchar(128),
EMAIL VARCHAR(128),
DESCRIPTION VARCHAR(128));

CREATE TABLE USERCALENDAR (
  USERCALENDAR_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
  CALENDAR_ID INTEGER, 
  USER_ID INTEGER,
  WORK_START VARCHAR(100),
  WORK_END VARCHAR(100),
  DESCRIPTION VARCHAR(200)
);

CREATE TABLE USERROLE (
  USERROLE_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
  USER_ID INTEGER,
  ROLENAME VARCHAR(45) DEFAULT NULL,
  EMAIL VARCHAR(128) DEFAULT NULL
);